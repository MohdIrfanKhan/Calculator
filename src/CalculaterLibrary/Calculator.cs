namespace CalculaterLibrary
{
    public class Calculator
    {
        /// <summary>
        ///     Add two numbers.
        /// </summary>
        /// <returns></returns>
        public static int Add(int x, int y)
        {
            return x + y;
        }

        /// <summary>
        ///     Subtract two numbers.
        /// </summary>
        /// <returns></returns>
        public static int Subtract(int x, int y)
        {
            return x - y;
        }

        /// <summary>
        ///    Multiply two numbers.
        /// </summary>
        /// <returns></returns>
        public static int Multiply(int x, int y)
        {
            return x * y;
        }

        /// <summary>
        ///     Divide two numbers.
        /// </summary>
        /// <returns></returns>
        public static float Divide(int x, int y)
        {
            return x / y;
        }
    }
}
